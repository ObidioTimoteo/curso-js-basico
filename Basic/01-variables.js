// var
// Ahora no se recomienda usar var, pero lo dejamos para que veas la diferencia con let y const

var cat ="Timoteo"
console.log("Me llamo " + cat)

cat = "Obidio"
console.log("Me llamo " + cat)

// let
// Sólo son accesibles dentro del bloque donde se declaran
let dog = "Rufus"
console.log("Me llamo " + dog)

dog = "Max"
console.log("Me llamo " + dog)

// const
const fish = "Nemo"
console.log("Me llamo " + fish)

